import random  # random module can through any random variable based on it's functions in the range mentioned, as below

print(random.randint(10,20))  # 10--20 included  , randint is one of the function imported by the module random
print(random.randrange(10,20)) #10 - 19 # start - (end -1)
print(random.randrange(10)) # 0 - 9 # start - (end -1)
print(random.randrange(10,20,3)) # 10, 13, 16, 19   
